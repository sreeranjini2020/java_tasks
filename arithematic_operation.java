
class ArithematicOperation {

    public static void main(String args[]) {
        int a = 37;
        int b = 12;

        int sum = a + b;
        System.out.println("The sum of 36 and 12:" + sum);

        int sub = a - b;
        System.out.println("The substraction of 36 and 12:" + sub);

        int mul = a * b;
        System.out.println("The multiplication of 36 and 12:" + mul);

        int div = a / b;
        System.out.println("The division of 36 by 12:" + div);

        int mode = a % b;
        System.out.println("The modulus of 36 by 12:" + mode);
    }

}
