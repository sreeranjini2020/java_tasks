
class Average {

    public static void main(String args[]) {
        float pro1, pro2, pro3, total, avg;
        pro1 = 35;
        pro2 = 69;
        pro3 = 98;

        total = pro1 + pro2 + pro3;
        avg = total / 3;
        System.out.println("Average of costs of 3 products:" + avg);
    }

}
