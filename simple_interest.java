
class SimpleInterest {

    public static void main(String args[]) {
        int prinicple = 789467;
        int timeperiod = 2;
        int rate = 14;

        int SI = (prinicple * timeperiod * rate) / 100;
        System.out.println("The SI for 2years with 14% rate of interest for a prinicple of Rs 789467 :" + SI);
    }

}
